"use client";

import { useControllableState } from "@radix-ui/react-use-controllable-state";
import { Badge } from "@/components/ui/badge";
import {
  Collapsible,
  CollapsibleContent,
  CollapsibleTrigger,
} from "@/components/ui/collapsible";
import { cn } from "@/lib/utils";
import type { LucideIcon } from "lucide-react";
import { BrainIcon, ChevronDownIcon, DotIcon } from "lucide-react";
import type { ComponentProps, ReactNode } from "react";
import { createContext, memo, useContext, useMemo } from "react";

interface ChainOfThoughtContextValue {
  isOpen: boolean;
  setIsOpen: (open: boolean) => void;
}

const ChainOfThoughtContext = createContext<ChainOfThoughtContextValue | null>(
  null
);

const useChainOfThought = () => {
  const context = useContext(ChainOfThoughtContext);
  if (!context) {
    throw new Error(
      "ChainOfThought components must be used within ChainOfThought"
    );
  }
  return context;
};

export type ChainOfThoughtProps = ComponentProps<"div"> & {
  open?: boolean;
  defaultOpen?: boolean;
  onOpenChange?: (open: boolean) => void;
};

export const ChainOfThought = memo(
  ({
    className,
    open,
    defaultOpen = false,
    onOpenChange,
    children,
    ...props
  }: ChainOfThoughtProps) => {
    const [isOpen, setIsOpen] = useControllableState({
      defaultProp: defaultOpen,
      onChange: onOpenChange,
      prop: open,
    });

    const chainOfThoughtContext = useMemo(
      () => ({ isOpen, setIsOpen }),
      [isOpen, setIsOpen]
    );

    return (
      <ChainOfThoughtContext.Provider value={chainOfThoughtContext}>
        <div className={cn("not-prose w-full space-y-1", className)} {...props}>
          {children}
        </div>
      </ChainOfThoughtContext.Provider>
    );
  }
);

export type ChainOfThoughtHeaderProps = ComponentProps<
  typeof CollapsibleTrigger
>;

export const ChainOfThoughtHeader = memo(
  ({ className, children, ...props }: ChainOfThoughtHeaderProps) => {
    const { isOpen, setIsOpen } = useChainOfThought();

    return (
      <Collapsible onOpenChange={setIsOpen} open={isOpen}>
        <CollapsibleTrigger
          className={cn(
            "flex w-full items-center gap-1.5 text-muted-foreground text-xs transition-colors hover:text-foreground",
            className
          )}
          {...props}
        >
          <BrainIcon className="size-3.5 shrink-0" />
          <span className="flex-1 text-left">
            {children ?? "Chain of Thought"}
          </span>
          <ChevronDownIcon
            className={cn(
              "size-3 shrink-0 transition-transform",
              isOpen ? "rotate-180" : "rotate-0"
            )}
          />
        </CollapsibleTrigger>
      </Collapsible>
    );
  }
);

export type ChainOfThoughtStepProps = ComponentProps<"div"> & {
  icon?: LucideIcon;
  label: ReactNode;
  description?: ReactNode;
  status?: "complete" | "active" | "pending";
  /** When set, step children render inside a collapsible panel toggled by the label. */
  defaultOpen?: boolean;
};

const stepStatusStyles = {
  active: "text-foreground",
  complete: "text-muted-foreground",
  pending: "text-muted-foreground/50",
};

export const ChainOfThoughtStep = memo(
  ({
    className,
    icon: Icon = DotIcon,
    label,
    description,
    status = "complete",
    defaultOpen = false,
    children,
    ...props
  }: ChainOfThoughtStepProps) => {
    const hasChildren = children != null;
    const rowClassName = cn(
      "flex gap-1.5 text-xs leading-snug",
      stepStatusStyles[status],
      "fade-in-0 slide-in-from-top-2 animate-in",
      className
    );

    if (!hasChildren) {
      return (
        <div className={rowClassName} {...props}>
          <div className="relative mt-px shrink-0">
            <Icon className="size-3" />
          </div>
          <div className="min-w-0 flex-1 space-y-0.5 overflow-hidden">
            <div>{label}</div>
            {description && (
              <div className="text-muted-foreground text-xs">{description}</div>
            )}
          </div>
        </div>
      );
    }

    return (
      <Collapsible
        className={cn("group", rowClassName)}
        defaultOpen={defaultOpen}
        {...props}
      >
        <div className="relative mt-px shrink-0">
          <Icon className="size-3" />
        </div>
        <div className="min-w-0 flex-1 space-y-0.5 overflow-hidden">
          <CollapsibleTrigger
            className={cn(
              "flex w-full items-center gap-2 text-left transition-colors",
              "cursor-pointer hover:text-foreground"
            )}
          >
            <span className="flex-1">{label}</span>
            <ChevronDownIcon
              className={cn(
                "size-3 shrink-0 text-muted-foreground transition-transform",
                "group-data-[state=open]:rotate-180"
              )}
            />
          </CollapsibleTrigger>
          {description && (
            <div className="text-muted-foreground text-xs">{description}</div>
          )}
          <CollapsibleContent
            className={cn(
              "space-y-1 outline-none",
              "data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0",
              "data-[state=closed]:animate-out data-[state=open]:animate-in"
            )}
          >
            {children}
          </CollapsibleContent>
        </div>
      </Collapsible>
    );
  }
);

